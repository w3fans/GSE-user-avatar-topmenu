import fs from 'node:fs';

const source = fs.readFileSync('prefs.js', 'utf8');
const strings = new Set();
const add = value => {
    if (value && !value.includes('://'))
        strings.add(value.replace(/\\'/g, "'"));
};

for (const match of source.matchAll(
    /(?:title|subtitle|description|label):\s*'((?:\\'|[^'])*)'/g
))
    add(match[1]);

for (const match of source.matchAll(
    /this\._add(?:BoundSwitch|PollingIntervalRow|DeviceRow)\(([^;]+)\);/gs
)) {
    const values = [...match[1].matchAll(/'((?:\\'|[^'])*)'/g)].map(value => value[1]);
    for (const value of values.slice(1))
        add(value);
}

for (const match of source.matchAll(/\[\s*'((?:\\'|[^'])*)'/g)) {
    const value = match[1];
    if (/^[A-Z]|\s/.test(value) && !value.endsWith('-symbolic'))
        add(value);
}

const escape = value => value.replace(/\\/g, '\\\\').replace(/'/g, "\\'");
const output = [
    '// Generated by po/update-pot.sh; do not edit.',
    'const _ = text => text;',
    '',
    ...[...strings].sort().map(value => `_('${escape(value)}');`),
    '',
].join('\n');
fs.writeFileSync('po/source-strings.js', output);
